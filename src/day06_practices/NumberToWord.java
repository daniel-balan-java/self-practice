package day06_practices;

public class NumberToWord {

    public static void main(String[] args) {

        int number = 0;

        String result = "";

        if (number == 0){
            result = "Zero";
        }
        if (number == 1){
            result = "One";
        }
        if (number == 2){
            result = "Two";
        }
        if (number == 3){
            result = "Three";
        }
        if (number == 4){
            result = "Four";
        }
        if (number == 5){
            result = "Five";
        }
        if (number == 6){
            result = "Six";
        }
        if (number == 7){
            result = "Seven";
        }
        System.out.println(result);


    }
}

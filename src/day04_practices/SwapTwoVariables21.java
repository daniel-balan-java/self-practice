public class SwapTwoVariables21 {

    public static void main(String[] args) {

       int x = 10,
               y = 15;

       x = x + y; //25
       y = x - y; //10
       x = x - y; //15

        System.out.println("x = " + x);
        System.out.println("y = " + y);





    }
}

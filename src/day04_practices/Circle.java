package day04_practices;

public class Circle {

    public static void main(String[] args) {

        double radius = 5,
                area = 3.14 * radius * radius,
                perimeter = 2 * 3.14 * 5;

        System.out.println("Area of the circle is " + area);
        System.out.println("Perimeter of the circle is " + perimeter);

    }
}
